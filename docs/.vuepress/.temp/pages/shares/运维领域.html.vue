<p>一个非常新鲜的网络通信中间件框架，可以说在很多方面都在对标 <code v-pre>Nginx</code> ，不过其更多的是为大型SOA（面向服务架构）设计的，在如今这个云服务分布式服务大流行的背景下表现的非常不错。</p>
